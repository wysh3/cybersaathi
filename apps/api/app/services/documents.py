"""Document generation services for CyberSaathi.

Generates editable NCRP complaint drafts, bank dispute emails, evidence
timelines, RTI drafts, journalist digests, and infographic copy. The
documents are always clearly marked as drafts and never impersonate
official submission.
"""

from __future__ import annotations

