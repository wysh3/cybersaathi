def generate_victim_notification(cluster: ClusterRecord) -> GeneratedDocument:
    """Short notification a victim in the cluster could forward to themselves.

    No PII; we tell the user their report is part of an escalated cluster
    and point them at the journalist digest, RTI draft, and the official
    1930 helpline.
    """

    body = f"""DRAFT — Victim Notification

Your case is part of an escalated pattern.

CyberSaathi's accountability engine has identified {cluster.report_count} "
"unresolved {cluster.fraud_type.replace('_', ' ')} reports across "
"{len(cluster.districts)} districts in {', '.join(cluster.states)}. "
"Because the pattern has crossed the 50-report, 30-day threshold with no "
"FIR or resolution status, CyberSaathi has generated:

- A public accountability alert
- A journalist digest (cluster-level counts only)
- A pre-filled RTI draft addressed to the relevant state cyber cell
- A shareable infographic

You can:
1. Continue to file your NCRP complaint at cybercrime.gov.in.
2. Forward the journalist digest to a local reporter if you choose to.
3. Submit the RTI draft to the public information officer of the
   relevant state cyber cell.

You are not alone. The pattern is now visible.
"""
    return GeneratedDocument(
        id=_new_id("doc"),
        complaint_id=cluster.id,
        kind=DocumentKind.VICTIM_NOTIFICATION,
        title=f"Victim Notification — {cluster.fraud_type} cluster",
        editable_body=body.strip(),
        created_at=_now(),
        export_status="draft",
    )
